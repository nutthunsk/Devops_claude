const DICTS = { en, th }

const interpolate = (str, params) =>
  params ? str.replace(/\{(\w+)\}/g, (m, k) => (params[k] != null ? params[k] : m)) : str

export function makeT(lang) {
  const dict = DICTS[lang] || en
  const t = (key, params) => {
    const val = key.split('.').reduce((o, k) => (o == null ? o : o[k]), dict)
    if (typeof val !== 'string') return key
    return interpolate(val, params)
  }
  // Translate a data label (category / account type / portfolio / currency name)
  const tl = (kind, value) => (dict.labels[kind] && dict.labels[kind][value]) || value
  return { t, tl }
}
